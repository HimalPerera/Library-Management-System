    public Object[][] searchByBookID(int bookID) throws SQLException {
        //searches the issued_book table for given book ID
        try {
            String query = "SELECT * FROM issued_book WHERE Book_ID = " + "'" + bookID + "'";

            statement = connection.createStatement();
            rsltSet = statement.executeQuery(query);

            //if the result set is not empty
            if (rsltSet.next()) {
                haveBooks = true;

                //counts the number of books by given book ID
                rsltSet.beforeFirst();
                while (rsltSet.next()) {
                    NoOfBooks++;
                }

                //inserts details of all books in to arrays
                bookDetails = new Object[NoOfBooks][10];
                rsltSet.beforeFirst();
                for (int i = 0; i < NoOfBooks; i++) {
                    rsltSet.next();

                    bookDetails[i][0] = i + 1;
                    bookDetails[i][1] = rsltSet.getInt(1);
                    bookDetails[i][2] = rsltSet.getString(2);
                    bookDetails[i][3] = rsltSet.getString(3);
                    bookDetails[i][4] = rsltSet.getInt(7);
                    bookDetails[i][5] = rsltSet.getString(8);
                    bookDetails[i][6] = rsltSet.getString(9);
                    bookDetails[i][7] = rsltSet.getString(10);
                    bookDetails[i][8] = rsltSet.getInt(11);
                    bookDetails[i][9] = rsltSet.getDate(12);
                }

                statement.close();
                rsltSet.close();
            } else {
                haveBooks = false;
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex);
        }

        return bookDetails;
    }

