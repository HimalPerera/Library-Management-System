    private void loadToTable(Object bookDetails[][]) {
        DefaultTableModel table = (DefaultTableModel) jTable1.getModel();
        table.setNumRows(0);

        for (int i = 0; i < NoOfBooks; i++) {
            Object[] row = {bookDetails[i][0], bookDetails[i][1], bookDetails[i][2], bookDetails[i][3], bookDetails[i][4], bookDetails[i][5], bookDetails[i][6], bookDetails[i][7], bookDetails[i][8], bookDetails[i][9]};

            table.addRow(row);
        }

    }

