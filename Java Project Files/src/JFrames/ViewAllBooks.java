    private void jBtnSearchMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jBtnSearchMouseClicked
        NoOfBooks = 0;
        haveBooks = false;

        if (jRadioButtonTitle.isSelected()) {
            String searchTitle = jTxtSearch.getText();

            if (!jTxtSearch.getText().equals("")) {
                //searces the database for given title
                try {
                    searchByTitle(searchTitle);
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, ex);
                }

                if (haveBooks) {
                    loadToTable(bookDetails);
                    if (NoOfBooks == 1) {
                        JOptionPane.showMessageDialog(null, "Found 1 book by given title");
                    } else if (NoOfBooks > 1) {
                        JOptionPane.showMessageDialog(null, "Found (" + NoOfBooks + ") books by given title");
                    }
                } else {
                    DefaultTableModel table = (DefaultTableModel) jTable1.getModel();
                    table.setNumRows(0);
                    JOptionPane.showMessageDialog(null, "No books found by given title");
                }
            } else if (jTxtSearch.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Enter the title");
            }
        }

        if (jRadioButtonAuthor.isSelected()) {
            String searchAuthor = jTxtSearch.getText();

            if (!jTxtSearch.getText().equals("")) {
                //searches the database for given author
                try {
                    searchByAuthor(searchAuthor);
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, ex);
                }

                if (haveBooks) {
                    loadToTable(bookDetails);
                    if (NoOfBooks == 1) {
                        JOptionPane.showMessageDialog(null, "Found 1 book by given author");
                    } else if (NoOfBooks > 1) {
                        JOptionPane.showMessageDialog(null, "Found (" + NoOfBooks + ") books by given author");
                    }
                } else {
                    DefaultTableModel table = (DefaultTableModel) jTable1.getModel();
                    table.setNumRows(0);
                    JOptionPane.showMessageDialog(null, "No books found by given author");
                }
            } else if (jTxtSearch.getText().equals("")) {
                JOptionPane.showMessageDialog(null, "Enter the author");
            }
        }
    }//GEN-LAST:event_jBtnSearchMouseClicked

