    public Object[][] searchByTitle(String title) throws SQLException {
        //searches the book table for given title
        try {
            String query = "SELECT * FROM book WHERE Title = " + "'" + title + "'";

            statement = connection.createStatement();
            rsltSet = statement.executeQuery(query);

            //if the result set is not empty
            if (rsltSet.next()) {
                haveBooks = true;

                //counts the number of books by given title
                rsltSet.beforeFirst();
                while (rsltSet.next()) {
                    NoOfBooks++;
                }

                //inserts details of all books in to arrays
                bookDetails = new Object[NoOfBooks][7];
                rsltSet.beforeFirst();
                for (int i = 0; i < NoOfBooks; i++) {
                    rsltSet.next();

                    bookDetails[i][0] = rsltSet.getString(2);
                    bookDetails[i][1] = rsltSet.getString(3);
                    bookDetails[i][2] = rsltSet.getInt(1);
                    bookDetails[i][3] = rsltSet.getString(5);
                    bookDetails[i][4] = rsltSet.getInt(4);
                    bookDetails[i][5] = rsltSet.getInt(6);
                    bookDetails[i][6] = rsltSet.getDouble(7);
                }

                statement.close();
                rsltSet.close();
            } else {
                haveBooks = false;
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex);
        }

        return bookDetails;
    }

